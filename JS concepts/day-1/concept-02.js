// working with conditional using Ternary Operator

let age = 23;
let fname = age > 10 ? "Sidharth" : "default";
let demo1 = age > 20 && "Sid";
console.log(demo1);
// && --> show string if variable is true

let demo2 = age > 20 || "Sid";
console.log(demo2);
// || --> returns boolean true or false

console.log(fname);

